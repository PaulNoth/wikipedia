package vi_dictionary;


public class LanguageLink {
    private String name;
    private String link;
    
    public String GetName()
    {
        return name;
            }
    public void SetName(String value)
    {
        name = value;
    }
    
    public String GetLink()
    {
        return link;
            }
    public void SetLink(String value)
    {
        link = value;
    }
    
}
