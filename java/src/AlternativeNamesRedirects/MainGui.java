    private void jList2ValueChanged(javax.swing.event.ListSelectionEvent evt) {                                    
    	String Selected = null;
    	try{
        	Selected = jList2.getSelectedValue().toString();
        }catch(NullPointerException ex){}
        String[] help = Selected.split(":");
        ReverseRedirects res = HashMap.get(help[1]);
        int dead;
        DefaultListModel listModel = new DefaultListModel(); 
        if(res.isDis){
        	listModel.addElement("This page is disambigous");
        	String[] hlp = res.deadDisambigs.split(",");
        	dead = res.countDisam - hlp.length;
        	listModel.addElement("Number of disamb. redirects: "+res.countDisam+", "+dead+" of which alive");
            listModel.addElement("\t Disamb. redirects "+res.disambgis+"---- dead ----:"+res.deadDisambigs);
            
        }

        listModel.addElement("Number of name redirects: "+res.count+", number of all red:"+res.count);
        listModel.addElement("\t Name redirects: "+res.reversers);

        listModel.addElement("Number of redirects on page comparing to average:"+(res.count - average));
        jList3.setModel(listModel);
    } 
    
