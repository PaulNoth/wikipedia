   // event for file chooser
    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {                                      
        FileFilter filter = new FileNameExtensionFilter("XML File", "xml");
        jFileChooser1.addChoosableFileFilter(filter);
        jFileChooser1.setAcceptAllFileFilterUsed(false);
        jFileChooser1.setFileFilter(filter);
        int returnVal = jFileChooser1.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = jFileChooser1.getSelectedFile();
            try {
              jLabel1.setText(file.getAbsolutePath());
              jButton2.setEnabled(true);
              wiki = file;
            } catch (Exception ex) {
              System.out.println("Problem accessing file "+ex.getMessage());
              JOptionPane.showMessageDialog(null, "Problem accessing file "+ex.getMessage());
            }
        } 
        else {
            System.out.println("File access cancelled by user.");
            JOptionPane.showMessageDialog(null, "You need to choose file!");
        }       
     
    }                                     

